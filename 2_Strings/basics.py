mystring = "Hello World"
print(mystring)

print(mystring[0])

# negative indexing
print(mystring[-2])
print(mystring[9])

#substring from 2nd index till end
print(mystring[2:])

#substring from 0th till 5th (note 6th idx is not included)
print(mystring[:6])

#from start (inclusive) to end (not inclusive)
print(mystring[2:8])

#step size default of 1
print(mystring[::])

#step size of 2
print(mystring[::2])

#reverse a String; negative step size
print(mystring[::-1])

print(mystring[-1:-12:-1])

